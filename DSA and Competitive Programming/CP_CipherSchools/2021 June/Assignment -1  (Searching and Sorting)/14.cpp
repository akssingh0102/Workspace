int maxSubArraySum(int *arr,int &n){
    int res=arr[0];
    int curr=arr[0];

    for (int i = 1; i < n; i++)
    {
        curr = max(curr+arr[i],arr[i]);
        res = max(res,curr);
    }
    return res;
}

