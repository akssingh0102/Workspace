struct Node
{
    int data;
    struct Node *left, *right;
};

Node *newNode(int data)
{
    Node *temp = new Node;
    temp->data = data;
    temp->left = temp->right = NULL;
    return temp;
}

void printLeftView(Node *root)
{
    if (!root)
        return;

    queue<Node *> q;
    q.push(root);

    while (!q.empty())
    {
        int n = q.size();

        for (int i = 1; i <= n; i++)
        {
            Node *temp = q.front();
            q.pop();

            if (i == 1)
                cout << temp->data << " ";

            if (temp->left != NULL)
                q.push(temp->left);

            if (temp->right != NULL)
                q.push(temp->right);
        }
    }
}

void printRightView(Node *root)
{
    if (!root)
        return;

    queue<Node *> q;
    q.push(root);

    while (!q.empty())
    {
        int n = q.size();

        for (int i = 1; i <= n; i++)
        {
            Node *temp = q.front();
            q.pop();

            if (i == n)
                cout << temp->data << " ";

            if (temp->left != NULL)
                q.push(temp->left);

            if (temp->right != NULL)
                q.push(temp->right);
        }
    }
}

void verticalTraversal(Node *root, int level, map<int, vector<int> > &levelNodes)
{
    if (root == NULL)
    {
        return;
    }
    levelNodes[level].push_back(root->data);
    verticalTraversal(root->left, level - 1, levelNodes);
    verticalTraversal(root->right, level + 1, levelNodes);
}

void verticalTraversal(Node *root)
{
    map<int, vector<int> > levelNodes;
    verticalTraversal(root, 0, levelNodes);
    for (auto itr = levelNodes.begin(); itr != levelNodes.end(); itr++)
    {
        for (int i = 0; i < itr->second.size(); i++)
        {
            cout << itr->second[i] << " ";
        }
        cout << "\n";
    }
}

